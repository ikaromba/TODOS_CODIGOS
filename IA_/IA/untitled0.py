        larg_sepala = float(input("Largura da sépala : "))
        if larg_sepala == -1: break

        comp_petala = float(input("Comprimento da pétala : "))
        if comp_petala == -1: break

        larg_petala = float(input("Largura da pétala : "))
        if larg_petala == -1: break

        # Organizando os dados digitados no formato que a IA aceita [[valores]]
        nova_flor = [[comp_sepala, larg_sepala, comp_petala, larg_petala]]

        # Fazendo a classificação
        resultado_id = modelo.predict(nova_flor)[0]
        nome_flor = nomes_classes[resultado_id]

        print(f"\n=> RESULTADO: Essa flor é da espécie: {nome_flor.upper()} (Classe {resultado_id})\n")
        #imprime uma "divisoria feita de "-"
        print("-" * 40)
