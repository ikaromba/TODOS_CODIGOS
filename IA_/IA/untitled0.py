from sklearn.datasets import load_iris
from sklearn.model_selection import train_test_split
from sklearn.tree import DecisionTreeClassifier

#Modelo de trinamento
print("Treinando o Modelo")
iris = load_iris()
X = iris.data  # Características das flores
y = iris.target  # tipos de flores 0, 1 ou 2
nomes_classes = iris.target_names
# variação das plantas e seus nomes setosa, versicolor, virginica

# Treinando o modelo diretamente com o Dataset
modelo = DecisionTreeClassifier(random_state=42)
modelo.fit(X, y)
print("Modelo treinado com sucesso")

#Fazer o teste com amostra para validar se o codigigo vai funcionar
print("Teste com Amostra Pré-existente ")

amostra_teste = [X[0]]  # seleção dos dados
classe_real = y[0]

previsao = modelo.predict(amostra_teste)[0]

print(f"Dados da amostra escolhida: {amostra_teste[0]}")
print(f"Classe real no dataset: {classe_real} ({nomes_classes[classe_real]})")
print(f"Classe prevista pela IA: {previsao} ({nomes_classes[previsao]})")

if previsao == classe_real:
    print("Resultado: Funcionou.Os resutados batem com o real.")
else:
    print("Resultado: Não funcionou.")

#Menu para o usuario cancelar a adiçãp de uma nova flor
print("Adição de uma nova flor a lista")
print("Digite as medidas da flor que deseja adicionar na classificar.\n digite -1 para sair da adição\n")

